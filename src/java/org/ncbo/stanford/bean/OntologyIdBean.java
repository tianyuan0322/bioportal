package org.ncbo.stanford.bean;

public class OntologyIdBean {

	private Integer ontologyId;

	/**
	 * @param ontologyId
	 */
	public OntologyIdBean(Integer ontologyId) {
		super();
		this.ontologyId = ontologyId;
	}

	/**
	 * @return the ontologyId
	 */
	public Integer getOntologyId() {
		return ontologyId;
	}
}
