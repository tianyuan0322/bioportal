package org.ncbo.stanford.bean;

public class NamespaceBean {
	private String prefix;
	private String uri;

	public NamespaceBean(String prefix, String uri) {
		this.prefix = prefix;
		this.uri = uri;
	}

	/**
	 * @return the prefix
	 */
	public String getPrefix() {
		return prefix;
	}

	/**
	 * @param prefix
	 *            the prefix to set
	 */
	public void setPrefix(String prefix) {
		this.prefix = prefix;
	}

	/**
	 * @return the uri
	 */
	public String getUri() {
		return uri;
	}

	/**
	 * @param uri
	 *            the uri to set
	 */
	public void setUri(String uri) {
		this.uri = uri;
	}
}
